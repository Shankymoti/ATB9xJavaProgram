package oct.ex_211024_ForLoop;

public class Lab092
{
    public static void main(String[] args) {

        for(float f=0.0f; f<2.9; f++){
            System.out.println(f);
        }

        System.out.println("==================");
        for(long i=0; i<10; i++){
            System.out.println(i);
        }
        System.out.println("====================");

        for (char c= 'a'; c<='z'; c++){
            System.out.println(c);
        }
    }
}
